<b><i>Sea of Strangers</i></b>

In a sea of strangers,
you've longed to know me.
Your life spent sailing
to my shores.
The arms that yearn
to someday hold me,
will ache beneath
the heavy oars.
Please take your time
and take it slowly;
as all you do
will run its course.
And nothing else
can take what only-
was always meant
as solely yours.