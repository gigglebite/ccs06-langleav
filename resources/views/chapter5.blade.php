<b><i>Contentment</i></b>

There was a time when I thought I wanted more. When I
saw the sky as a soaring highway and the world as one big
carousel. But those yearnings have grown so distant, ebbed
so far away from me, they feel as though they belong to
someone else. All I want now is a rainy day, a roaring fire, you
and I talking about how quickly the time has gone.